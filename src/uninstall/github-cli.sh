#!/usr/bin/env bash
set -e

# ------------------------------------------------------------------------------
# Require running as root (or via sudo)
# ------------------------------------------------------------------------------
if [ "$EUID" -ne 0 ]; then
  echo "Please run this script as root (e.g., 'sudo ./remove.sh')."
  exit 1
fi

# ------------------------------------------------------------------------------
# Remove the package
# ------------------------------------------------------------------------------
echo "Removing GitHub CLI..."
dnf -y remove gh gh-cli

# ------------------------------------------------------------------------------
# Completion message
# ------------------------------------------------------------------------------
echo "GitHub CLI removed successfully."
